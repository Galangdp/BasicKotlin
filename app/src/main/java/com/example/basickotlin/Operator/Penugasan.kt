package com.example.basickotlin.Operator

fun main() {
    var a = 10
    a = a + 10      // bisa juga gini a += 10
    println(a)      // pertambahan

    var b = 10
    b = b - 10      // bisa juga gini a -= 10
    println(b)      // pengurangan

    var c = 10
    c = c * 10      // bisa juga gini a *= 10
    println(c)      // perkalian

    var d = 10
    d = d / 10      // bisa juga gini a /= 10
    println(d)      // pembagian

    var e = 10
    e = e % 10      // bisa juga gini a %= 10 diskon atau sisa bagi
    println(e)      // modus atau



}